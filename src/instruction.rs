#[derive(Debug, Eq, PartialEq)]
pub enum Offset {
    None,
    X,
    Y,
}

#[derive(Debug, Eq, PartialEq)]
pub enum Operand {
    None,
    Immediate(u8),
    Relative(i8),
    Absolute(u16, Offset),
    ZeroPage(u8, Offset),
    Indirect(u16, Offset),
}

#[derive(Debug)]
pub struct Instruction {
    pub operation: Operation,
    pub operand: Operand,
}

